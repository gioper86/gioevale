<header>
    <h2>Getting to Palmi</h2>
    <p>How to get to Palmi</p>
</header>

<p>
    If you are not coming by car, our advice is to hire one as public 
    transport is not the best and moving around is not easy.
    If you don't drive it's still possible to get there :) In this page you will find 
    some information but if you need more just get in touch. 
</p>

<header>
    <h3>By air</h3>
</header>
<p>
    There are two airports near Palmi, <b>Reggio Calabria (REG)</b>, which is rather small 
    and <b>Lamezia Terme (SUF)</b>, which is served by many national and a few international flights 
    (also a direct flight from London).
</p>
